/**
 * add - adds two integers
 *
 * @a - first int
 * @b - second int
 *
 * Return: sum of ints.
 */

int add(int a, int b)
{
    return (a + b);
}


/**
 * sub - subtracts two integers
 *
 * @a - first int
 * @b - second int
 *
 * Return: a - b
 */



int sub(int a, int b)
{
	return (a - b);
}



/**
 * mul - multiplies two integers
 *
 * @a - first int
 * @b - second int
 *
 * Return: a * b
 */



int mul(int a, int b)
{
	return (a * b);
}



/**
 * div - div two integers
 *
 * @a - first int
 * @b - second int
 *
 * Return: a / b
 */



int div(int a, int b)
{
	return (a / b);
}



/**
 * mod - returns remainder after dividing two integers
 *
 * @a - first int
 * @b - second int
 *
 * Return: a % b
 */


int mod(int a, int b)
{
        return (a % b);
}
