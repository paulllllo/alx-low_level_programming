#include "3-calc.h"

/**
 * op_add - adds two numbers
 *
 * @a: the first integer
 * @b: The second integer
 *
 * Return: Returns the addition of integers
 */

int op_add(int a, int b)
{
	return (a + b);
}


/**
 * op_sub - A function That subtracts integers
 *
 * @a: The first integer
 * @b: The second integer
 *
 * Return: Returns a - b
 */

int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_mul - A function thatmultipplies integers
 *
 * @a: The first integer
 * @b: The second integer
 *
 * Return: Returns a * b
 */

int op_mul(int a, int b)
{
	return (a * b);
}


/**
 * op_div - A function that divides two integers
 *
 * @a: The first integer
 * @b: The second integer
 *
 * Return: Returns a/b
 */

int op_div(int a, int b)
{
	return (a / b);
}


/**
 * op_mod - A function that calculates the  modulus of two integers
 *
 * @a: The first integer
 * @b: The second integer
 *
 * Return: Returns a % b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
